export const set_clothes = function (container) {
	clothes.forEach((item) => {
		const li = document.createElement('li');

		if (item.options) {
			const ul = document.createElement('ul');

			const category = document.createElement('li');
			category.classList.add('category');
			category.innerText = item.category;
			li.appendChild(category);

			item.options.forEach((_item) => {
				Object.keys(_item.data).forEach((sex) => {
					const _li = document.createElement('li');
					_li.setAttribute('data-sex', sex);

					const option = document.createElement('input');
					option.type = 'radio';
					option.name = 'clothes=' + sex;
					option.value = _item.data[sex];

					const label = document.createElement('label');
					label.innerText = _item.name;

					_li.appendChild(option);
					_li.appendChild(label);

					ul.appendChild(_li);
				});
			});

			li.appendChild(ul);
			container.appendChild(li);
		} else {
			const option = document.createElement('input');
			option.type = 'radio';
			option.name = 'clothes';
			option.value = item;

			const label = document.createElement('label');
			label.innerText = item.name;

			li.appendChild(option);
			li.appendChild(label);

			container.appendChild(li);
		}
	});
};

export default clothes;
